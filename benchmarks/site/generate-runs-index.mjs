import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const siteDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(siteDir, "..", "..");
const runsDir = path.join(repoRoot, "benchmarks", "runs");
const outputPath = path.join(runsDir, "index.js");

fs.mkdirSync(runsDir, { recursive: true });

const entries = fs
  .readdirSync(runsDir, { withFileTypes: true })
  .filter((entry) => entry.isFile() && entry.name.endsWith(".json"))
  .sort((left, right) => left.name.localeCompare(right.name))
  .map((entry) => {
    const filePath = path.join(runsDir, entry.name);
    const run = JSON.parse(fs.readFileSync(filePath, "utf8"));
    run.source_file = entry.name;
    return {
      source_file: entry.name,
      run,
    };
  })
  .sort((left, right) => {
    const startedDelta = Number(left.run.started_at || 0) - Number(right.run.started_at || 0);
    return startedDelta || left.source_file.localeCompare(right.source_file);
  });

const content = `// Generated by benchmarks/site/generate-runs-index.mjs.
// Do not edit by hand.
window.NAVI_BENCHMARK_RUNS = ${JSON.stringify(entries, null, 2)};
`;

fs.writeFileSync(outputPath, content);
console.log(`Indexed ${entries.length} benchmark run(s) in ${path.relative(repoRoot, outputPath)}`);
